#include <unistd.h>

void    ft_putstr(char *str)
{
    while (*str != '\0')
    {
        write (1, str, 1);
        str++;
    }
}

int main(void)
{
    char a[] = "hello world";
    ft_putstr(a);
    write (1, "\n", 1);
    return 0;
}

