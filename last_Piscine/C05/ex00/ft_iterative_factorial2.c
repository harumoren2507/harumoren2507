#include <stdio.h>

int ft_iterative_factorial(int  nb)
{
    int digits;
    
    digits = 1;
    
    if (nb < 0)
        return 0;
    while (nb < 1)
    {
        digits *= nb;
        nb--;
    }
    return (digits);
}

int main(void)
{
    printf("%d\n", ft_iterative_factorial(3));
    return 0;
}


