#include <stdio.h>

int ft_iterative_factorial(int  nb)
{
    if (nb < 0)
        return 0;
    else if (nb <= 1)
        return 1;
    return ft_iterative_factorial(nb - 1) * nb;
}


int main(void)
{
    printf ("%d\n", ft_iterative_factorial(4));
    return 0;
}


