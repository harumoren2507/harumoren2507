struct s_list
{
    int data;
    t_list  *next;
};

t_list  *sort_list(t_list*  lst, int(*cmp)(int, int))
{
    int swap;
    t_list *tmp;

    tmp = lst;
    while (lst->next != 0)
    {
        if (((*cmp)(lst->data, lst->next->data) == 0))// 0 = 偽なら入れ替える
        {
            swap = lst->data;
            lst->data = lst->next->data;
            lst->next->data = swap;
            lst = tmp;
        }
        else
            lst = lst->next;
    }
    lst = tmp;
    return (lst);
}

