#include <unistd.h>

int     ft_is_space(char i)
{
        if (i == '\t' || i == '\n' || i == '\n' || i == '\v' || i =='\f' || i == ' ')
                return 1;
        return 0;
}

void    first_word(char *str)
{
        while (ft_is_space(*str))
        {
                str++;
        }
        while (*str && !ft_is_space(*str))
        {
                write (1, str, 1);
                str++;
        }
}

int     main(int argc, char *argv[])
{
        if (argc == 2)
        {
                first_word(argv[1]);
                write (1, "\n", 1);
        }
        else
                write (1, "\n", 1);
}


/*
プログラム概要
はい、その通りです。このプログラムは、最初に出会う空白（スペースやタブなど）をスキップして、
その後に続く最初の単語を出力します。

最初の単語の出力後に続く空白は無視され、
最初の単語だけが出力された後にプログラムは改行を出力して終了します。

これにより、入力された文字列から最初の単語のみが抽出されて表示されることになります。

*/




/*
*この行のコード while (ft_isspace(*str)) は、
C言語で書かれたプログラムの一部で、
特定の条件下で繰り返し処理（ループ）を行うために使われます。

}ここで使われている while は「ループ」を意味し、
ft_isspace は「空白文字かどうかを判定する関数」です。

*str は、文字列 str の現在の文字を指します。
それでは、このコードの意味をもっと詳しく見ていきましょう。

while ループ
while はC言語の基本的な構文の一つで、「ある条件が真（true）である間、続けて中の処理を行う」という意味です。
条件が偽（false）になると、ループから抜け出して次のコードに進みます。

ft_isspace 関数
ft_isspace は、引数として受け取った文字が空白文字（スペース、タブ、改行など）かどうかを判定する関数です。
この関数は、空白文字であれば 1 （真）を、そうでなければ 0 （偽）を返します。

*str
str は文字列を指すポインタ（アドレス）です。
*str はポインタ str が指す場所の値、つまり文字列の「現在の文字」を表します。

具体的な意味
while (ft_isspace(*str)) この行は、「str が指す文字が空白文字である限り、ループを続ける」という意味です。
空白文字が見つかると、ft_isspace は 1 を返し、ループは続きます。

str を1文字進める（例えば str++）などの処理をループ内で行うことで、文字列の次の文字に移動します。
空白でない文字に到達すると、ft_isspace は 0 を返し、while ループは終了します。

つまり、このコードは「文字列の先頭にある一つまたは複数の空白文字をスキップする」という動作をします。
これにより、文字列の先頭にある空白を無視して、最初の非空白文字から処理を開始することができます。
*/