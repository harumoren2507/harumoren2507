int ft_is_space(char to_find, char *str)
{
    while (*str != '\0')
    {
        if (*str == to_find)
            return 1;
        str++;
    }
    return 0;
}
//


int ft_wordcount(char *str, char *charset)
{
    int count;
    int flag;

    count = 0;
    flag = 1;
    while (*str != '\0')
    {
        if (ft_is_space(*str, charset))
            flag = 1;
        if (!ft_is_space(*str, charset))
        {
            if (flag)
                count++;
            flag = 0;
        } 
        str++; 
    }
    return (count);
}



