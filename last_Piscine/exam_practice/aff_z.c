#include <unistd.h>
int main(int argc, char *argv[])
{
        int i;
        i = 0;

        if (argc == 2)
        {
                while (argv[1][i] != '\0')
                {
                        if (argv[1][i] == 'z')
                        {
                                write (1, "z", 1);
                                write (1, "\n", 1);
                                return 0;
                        }
                        i++;
                }
                write (1, "z", 1);
                write (1, "\n", 1);
        }
        else
        {
                write (1, "z", 1);
                write (1, "\n", 1);
        }
}

/*
このプログラムは、文字列を受け取って、その中で最初に見つかった 'z' という文字を表示します。
そして、その後に改行（新しい行に移ること）をします。

もし、文字列の中に 'z' という文字が一つもない場合は、'z' と表示して、

それから改行します。また、もしプログラムに与えられた文字列の数が1つではない場合

（つまり、文字列が全くない場合や、2つ以上の文字列がある場合）も、
'z' と表示してから改行します。

ここで大事な点を小学生にもわかるように説明すると：

文字列とは、文字が並んでいるものを言います。たとえば、
「apple」や「hello」などが文字列です。

最初に見つかった 'z' というのは、
文字列の中で最初に 'z' が出てくる場所を見つけることです。
たとえば、「pizza」の場合は2つ目の文字が最初の 'z' です。

改行とは、字を書いている行の終わりから、次の新しい行に移ることです。
まるで、本を読むときに行の終わりに来たら次の行に目を移すようなものです。

プログラムに与えられた文字列の数が1つというのは、
このプログラムが正しく動くためには、

ちょうど1つの文字列を受け取る必要があるということです。
もし、文字列が全くない場合や、2つ以上ある場合は、条件に合わないので、
結果は同じで「z」と表示して改行します。

このプログラムの目的は、与えられた文字列の中で 'z' を見つけてそれを表示すること、

または 'z' がないことを知らせることです。そして、文字列がちょうど1つでないときも、
「z」と教えてくれます。

*/